package com.example.spring.json.springjson.entity;


public class Comment {
    private float postId;
    private float postid=postId;
    private float id;
    private String name;
    private String email;
    private String body;

    public float getPostid() {
        return postid;
    }

    public void setPostid(float postid) {
        this.postid = postid;
    }

    public float getPostId() {
        return postId;
    }

    public void setPostId(float postId) {
        this.postId = postId;
    }

    public float getId() {
        return id;
    }

    public void setId(float id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

}
